#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char		*dest;
	const char	*source;

	dest = dst;
	source = src;
	if (!dest && !source)
		return (NULL);
	while (n--)
	{
		*(dest++) = *(source++);
	}
	return (dst);
}
